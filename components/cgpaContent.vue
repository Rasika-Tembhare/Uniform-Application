  <h2 class="text-2xl text-black py-4">How to calculate CGPA?</h2>
  <p>The Central Board of Secondary Education has a curriculum where 5 subjects are compulsary and one is
      optional.<br><br>
      <b>• TO CALCULATE AVERAGE CGPA</b><br>
      Assume the grade points of the 5 subjects as:<br>
      S1,S2,S3,S4,S5<br>
      Grade point(GP)=S1+S2+S3+S4+S5<br>
      CGPA=GP/5<br>
      <br>
      EXAMPLE:<br>
      Subject 1 Grades= 10<br>
      Subject 2 Grades=9<br>
      Subject 3 Grades=10<br>
      Subject 4 Grades=8<br>
      Subject 5 Grades=10<br>
      GP=10+9+10+8+10=47<br>
      "CGPA=47/5=9.4"<br><br>
      <b>• TO CALCULATE PERCENTAGE USING CGPA OBTAINED</b><br>
      Percentage=(CGPAx9.5)<br><br>

      EXAMPLE:<br>
      CGPA=9.4<br>
      Percentage=9.4x9.5=89.3<br><br>

      <b>• TO CALCULATE CGPA USING THE PERCENTAGE</b><br>
      CGPA=(Percentage/9.5)<br><br>

      EXAMPLE:<br>
      Percentage=89.3<br>
      CGPA=89.3/9.5=9.4<br>

